#!/usr/bin/env bash
# sync-announcement-core.sh — vendor the shared @ccg/announcement-* packages
# into the plugin webview without publishing them to npm.
#
# WHY: The shared announcement code lives in the www repo:
#   - www/packages/announcement-core — pure, framework-agnostic rules (schema,
#     eligibility, restricted-markdown parsing, urlSafety, icon whitelist).
#   - www/packages/announcement-ui   — the React presentation component
#     (AnnouncementView + Body/Media/icons) rendered by BOTH the www admin
#     preview and the plugin webview, so the two are pixel-identical.
# The plugin webview must render/decide exactly like the www admin so the two
# never disagree. Rather than publish npm packages, we copy the source verbatim
# into `webview/src/vendor/` and treat it as read-only vendored code (edit the
# www original, then re-run this).
#
# The copy is idempotent: each destination is wiped and rebuilt on every run, so
# deletions in the source are reflected and re-running produces no diff.
#
# Usage: bash ./scripts/build.sh sync-announcement-core
#    (or: bash ./scripts/sync-announcement-core.sh)
set -euo pipefail

REPO_ROOT="$(cd "$(dirname "$0")/.." && pwd)"

# This repo is a git worktree; the www checkout lives in the MAIN working tree,
# which `git worktree list --porcelain` always prints first. Capture the full
# listing first, then extract the first `worktree ` line — piping straight into
# `head` would SIGPIPE-fail git under `set -o pipefail`.
WORKTREES="$(git -C "$REPO_ROOT" worktree list --porcelain)"
MAIN_WORKTREE="$(printf '%s\n' "$WORKTREES" | awk '/^worktree /{sub(/^worktree /, ""); print; exit}')"
if [[ -z "$MAIN_WORKTREE" ]]; then
  echo "sync-announcement-core: could not resolve the main worktree via 'git worktree list'." >&2
  exit 1
fi

WWW_PACKAGES="$MAIN_WORKTREE/www/packages"

# The AUTO-GENERATED header prepended to every vendored source file, so anyone
# opening one sees it is a copy and must not be edited here.
read -r -d '' HEADER <<'EOF' || true
/**
 * AUTO-GENERATED by scripts/sync-announcement-core.sh — DO NOT EDIT.
 * Source of truth: www/packages/<pkg>/src (edit there, then re-run
 *   `bash ./scripts/build.sh sync-announcement-core`).
 */
EOF

# sync_pkg <pkg-name> <rewrite-core-import?>
#   Copies www/packages/<pkg>/src/*.{ts,tsx} (excluding __tests__) into
#   webview/src/vendor/<pkg>/, prepending the generated header. When the second
#   arg is non-empty, rewrites the `@ccg/announcement-core` package import to the
#   vendored path (`@/vendor/announcement-core`) so cross-package imports resolve
#   inside the webview bundle (announcement-ui depends on announcement-core).
sync_pkg() {
  local pkg="$1"
  local rewrite="${2:-}"
  local src_dir="$WWW_PACKAGES/$pkg/src"
  local dest_dir="$REPO_ROOT/webview/src/vendor/$pkg"

  if [[ ! -d "$src_dir" ]]; then
    echo "sync-announcement-core: source not found at $src_dir" >&2
    echo "  (expected the www checkout in the main worktree: $MAIN_WORKTREE)" >&2
    exit 1
  fi

  echo "sync-announcement-core: $pkg"
  echo "  source: $src_dir"
  echo "  dest:   $dest_dir"

  # Wipe + recreate so removed source files disappear from the vendored copy.
  rm -rf "$dest_dir"
  mkdir -p "$dest_dir"

  local count=0
  local src name
  # Copy only top-level .ts/.tsx source files (the __tests__ directory is a
  # subdir, so these globs never match it).
  shopt -s nullglob
  for src in "$src_dir"/*.ts "$src_dir"/*.tsx; do
    name="$(basename "$src")"
    {
      printf '%s\n\n' "$HEADER"
      if [[ -n "$rewrite" ]]; then
        sed 's|@ccg/announcement-core|@/vendor/announcement-core|g' "$src"
      else
        cat "$src"
      fi
    } > "$dest_dir/$name"
    count=$((count + 1))
    echo "  + $pkg/$name"
  done
  shopt -u nullglob

  if [[ "$count" -eq 0 ]]; then
    echo "sync-announcement-core: no .ts/.tsx source files found in $src_dir" >&2
    exit 1
  fi

  # A README marks the directory as vendored for anyone browsing the tree.
  cat > "$dest_dir/README.md" <<EOF
# vendor/$pkg (AUTO-GENERATED — do not edit)

This directory is a **vendored copy** of the shared \`@ccg/$pkg\` package,
copied verbatim (with a generated header) by
\`scripts/sync-announcement-core.sh\` from the source of truth:

    www/packages/$pkg/src

Do **not** edit files here. To change it, edit the www original and re-run:

    bash ./scripts/build.sh sync-announcement-core

The plugin webview imports these modules so it stays identical to the www
admin — the two can never disagree.
EOF
  echo "  + $pkg/README.md"
  echo "sync-announcement-core: synced $count source file(s) for $pkg."
}

# announcement-core is pure (.ts, no cross-package import to rewrite).
sync_pkg announcement-core
# announcement-ui depends on announcement-core → rewrite that import to vendored.
sync_pkg announcement-ui rewrite

echo "sync-announcement-core: done."

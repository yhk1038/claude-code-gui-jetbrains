#!/usr/bin/env bash
# sync-announcement-core.sh — vendor the shared @ccg/announcement-core package
# into the plugin webview without publishing it to npm.
#
# WHY: The pure, framework-agnostic announcement rules (schema, eligibility,
# restricted-markdown parsing, urlSafety, icon whitelist) live in the www repo
# at `www/packages/announcement-core`. The plugin webview must obey the exact
# same rules as the www admin so the two never disagree. Rather than publish an
# npm package, we copy the source verbatim into `webview/src/vendor/` and treat
# it as read-only vendored code (edit the www original, then re-run this).
#
# The copy is idempotent: the destination is wiped and rebuilt on every run, so
# deletions in the source are reflected and re-running produces no diff.
#
# Usage: bash ./scripts/build.sh sync-announcement-core
#    (or: bash ./scripts/sync-announcement-core.sh)
set -euo pipefail

REPO_ROOT="$(cd "$(dirname "$0")/.." && pwd)"

# This repo is a git worktree; the www checkout lives in the MAIN working tree,
# which `git worktree list --porcelain` always prints first. Capture the full
# listing first, then extract the first `worktree ` line — piping straight into
# `head` would SIGPIPE-fail git under `set -o pipefail`.
WORKTREES="$(git -C "$REPO_ROOT" worktree list --porcelain)"
MAIN_WORKTREE="$(printf '%s\n' "$WORKTREES" | awk '/^worktree /{sub(/^worktree /, ""); print; exit}')"
if [[ -z "$MAIN_WORKTREE" ]]; then
  echo "sync-announcement-core: could not resolve the main worktree via 'git worktree list'." >&2
  exit 1
fi

SRC_DIR="$MAIN_WORKTREE/www/packages/announcement-core/src"
DEST_DIR="$REPO_ROOT/webview/src/vendor/announcement-core"

if [[ ! -d "$SRC_DIR" ]]; then
  echo "sync-announcement-core: source not found at $SRC_DIR" >&2
  echo "  (expected the www checkout in the main worktree: $MAIN_WORKTREE)" >&2
  exit 1
fi

echo "sync-announcement-core:"
echo "  source: $SRC_DIR"
echo "  dest:   $DEST_DIR"

# Wipe + recreate so removed source files disappear from the vendored copy.
rm -rf "$DEST_DIR"
mkdir -p "$DEST_DIR"

# The AUTO-GENERATED header prepended to every vendored .ts file, so anyone
# opening one sees it is a copy and must not be edited here.
read -r -d '' HEADER <<'EOF' || true
/**
 * AUTO-GENERATED by scripts/sync-announcement-core.sh — DO NOT EDIT.
 * Source of truth: www/packages/announcement-core/src (edit there, then re-run
 *   `bash ./scripts/build.sh sync-announcement-core`).
 */
EOF

count=0
# Copy only top-level source files (exclude the __tests__ directory).
for src in "$SRC_DIR"/*.ts; do
  [[ -e "$src" ]] || continue
  name="$(basename "$src")"
  dest="$DEST_DIR/$name"
  {
    printf '%s\n\n' "$HEADER"
    cat "$src"
  } > "$dest"
  count=$((count + 1))
  echo "  + $name"
done

if [[ "$count" -eq 0 ]]; then
  echo "sync-announcement-core: no .ts source files found in $SRC_DIR" >&2
  exit 1
fi

# A README marks the directory as vendored for anyone browsing the tree.
cat > "$DEST_DIR/README.md" <<'EOF'
# vendor/announcement-core (AUTO-GENERATED — do not edit)

This directory is a **vendored copy** of the shared, framework-agnostic
announcement rules package. It is copied verbatim (with a generated header) by
`scripts/sync-announcement-core.sh` from the source of truth:

    www/packages/announcement-core/src

Do **not** edit files here. To change the rules, edit the www original and
re-run:

    bash ./scripts/build.sh sync-announcement-core

The plugin webview imports these modules so it enforces the exact same schema,
eligibility, restricted-markdown parsing, urlSafety, and icon whitelist as the
www admin — the two can never disagree.
EOF
echo "  + README.md"

echo "sync-announcement-core: synced $count source file(s)."
